package com.example._20195527_20191417.beans;

public class ActoresBean {
    private int ID;
    private String nombre;
    private int cantCategorias;
    private int cantPeliculas;

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getCantCategorias() {
        return cantCategorias;
    }

    public void setCantCategorias(int cantCategorias) {
        this.cantCategorias = cantCategorias;
    }

    public int getCantPeliculas() {
        return cantPeliculas;
    }

    public void setCantPeliculas(int cantPeliculas) {
        this.cantPeliculas = cantPeliculas;
    }
}
